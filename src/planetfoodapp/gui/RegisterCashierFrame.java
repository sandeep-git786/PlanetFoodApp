   public int validateInput()
   {
     userId=txtUserId.getText();
     char []pwd=txtPassword.getPassword();
     char []rpwd=txtRPassword.getPassword();
        if(userId.isEmpty()||pwd.length==0||rpwd.length==0)
            return 0;
        
        else
        {
           password=String.valueOf(pwd);
           rpassword=String.valueOf(rpwd);
           if(password.equals(rpassword))
               return 1;
           else
               return -1;
        }
   }
