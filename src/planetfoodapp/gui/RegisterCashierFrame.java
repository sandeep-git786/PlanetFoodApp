    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
       int result=validateInput();
       if(result==0)
       {
        JOptionPane.showMessageDialog(null, "please fill all the details!", "ERROR",JOptionPane.INFORMATION_MESSAGE);   
        return;
       }
       else if(result==-1)
       {
        JOptionPane.showMessageDialog(null, "password do not match!", "ERROR",JOptionPane.INFORMATION_MESSAGE);   
        return;   
       }
       else
       {
           empId=jcEmpId.getSelectedItem().toString();
           e=employee.get(empId);
           userName=e.getEmpName();
           try
           {
               User usr=new User();
               usr.setPassword(password);
               usr.setUserType("Cashier");
               usr.setUserId(userId);
               boolean status=registerCashier(usr,e);
               if(status)
               {
                JOptionPane.showMessageDialog(null, "Registration successfull!", "success!",JOptionPane.INFORMATION_MESSAGE);
                clearAll();
               }
               else
               {
               JOptionPane.showMessageDialog(null, "Registration Denied!", "failure!",JOptionPane.INFORMATION_MESSAGE);
               return;
               }
           }
           catch(SQLException ex)
           {
            JOptionPane.showMessageDialog(null, "DB error in Adding Product", "ERROR",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();   
           }
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

