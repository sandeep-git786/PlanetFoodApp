    public void loadProductDetails()
    {
     try
     {
         TransactList=OrderDao.getTransactions();
         Object[] rows=new Object[2];
         DefaultTableModel model=(DefaultTableModel)jtTransact.getModel();
          for(Order O:TransactList)
          {
              rows[0]=O.getOrdId();
              rows[1]=O.getGrandTotal();
              model.addRow(rows);
          }
     }
      catch(SQLException ex)
      {
        JOptionPane.showMessageDialog(null,"Error while connecting to DB!","Exception",JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
      }
    }
